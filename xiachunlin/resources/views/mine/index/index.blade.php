							<p>我们中国人，按照西方人的说法，大多数人，相对于基督教徒，没有真正的宗教信仰。我们中有的人迷信仙、佛、鬼、神，大多数中国人会给死去的父母、长辈烧纸，有些人烧香拜佛，有些人算命，迷信八字，有些人相信面相和手相，有些人研究风水堪舆之学……这些都是或深或浅的信仰或者宗教行为，大多数属于原始宗教范畴。我理解，人类之于宗教，就像父母之于孩子的关系类似。表面上看，是父母生养了孩子，对孩子爱护有加，应该是孩子需要父母，实际上并非如此。真实的情况是，生养孩子，从中体验酸甜苦辣的情感，看着他们作为自己生命的一部分不断的成长，乃是父母自身情感和生命延续的要求——孩子最开始只是一堆蛋白质和水分，他们并没有有意识地来到这个世界，是父母有意识地把他们带到了这个世界。人类信仰宗教，与前面的情形类似。表面上看，作为信仰者，我们加入组织，按照神的意志和规定生活，为神尽忠，希冀获得他的恩宠，而实际上，神也被我们认定是万能的和道德完善的，既然如此，他并不需要我们的追随和赞美来满足他的控制欲和虚荣心。真实的情况是，我们作为生命短暂的智慧生物，能够意识到自己生命的微不足道，又对于这种人生的悲剧无可奈何，因此我们需要依附于一种能够在宇宙中具有更持久存在能力的存在，使我们的灵魂和精神获得永生和安宁。、
								信神，对神来说并不是至关重要的，而对于我们来说却是生死攸关的。总之，信仰是为了我们自身价值的实现。
								这种情况，适合于大多数个人信仰宗教的情况。
								在中国，由于有着特殊的历史和国情，我觉得大家尤其应该信仰基督教。
								我们知道，中国社会的组织结构不是普世社会性质的，这是中国人至今仍被世界上很多民族当成野蛮人的原因。事实上，这种社会制度也确实限制了中国人不断提升自己的各方面的修养。专制的制度为了维护其存在总是倾向于用奴化的手法来扩充其奴隶的数量。
							我无意于诋毁任何宗教及其神圣，因为以我的经验，这种诋毁有时确实会给诋毁者带来灾难，尽管这也许可以用偶然来解释。但是，我仍然觉得，如果你作为一个人，觉得自己对于自己的人生或者说生命感到困惑，需要在信仰和宗教里获得心灵的平静，那么我建议你最好信基督教，尤其是新教。</p>
